def call_claude(briefing_type: str, date_str: str) -> dict:
    client = anthropic.Anthropic(
        api_key=get_anthropic_api_key(),
        default_headers={"anthropic-beta": "prompt-caching-2024-07-31"},
    )

    system_prompt = KOSPI_SYSTEM_PROMPT if briefing_type == "kospi" else US_SYSTEM_PROMPT

    market_data = load_market_data(briefing_type)
    news_summary = load_news_summary(briefing_type)

    # Build user message: dynamic data (not cached)
    candidates_key = "kospi_candidates" if briefing_type == "kospi" else "us_candidates"
    # Pass only what's needed for analysis — exclude heavy market_data_js (sidebar only)
    analysis_data = {
        k: v for k, v in market_data.items()
        if k not in ("market_data_js",)  # sidebar data not needed for analysis
    }

    user_content = f"오늘 날짜: {date_str}\n\n"
    user_content += f"시장 데이터:\n{json.dumps(analysis_data, ensure_ascii=False, indent=2)}\n\n"
    if news_summary:
        user_content += f"뉴스 요약:\n{json.dumps(news_summary, ensure_ascii=False, indent=2)}\n"

    print(f"[call_claude] Calling Claude API (type={briefing_type}, date={date_str})")
    print(f"[call_claude] User message: ~{len(user_content)//4} tokens estimated")

    response = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=4096,
        system=[
            {
                "type": "text",
                "text": system_prompt,
                "cache_control": {"type": "ephemeral"},  # Cache the system prompt
            }
        ],
        messages=[
            {"role": "user", "content": user_content}
        ],
    )

    # Log cache usage if available
    usage = response.usage
    if hasattr(usage, "cache_creation_input_tokens"):
        print(f"[call_claude] Cache created: {usage.cache_creation_input_tokens} tokens")
    if hasattr(usage, "cache_read_input_tokens"):
        print(f"[call_claude] Cache hit: {usage.cache_read_input_tokens} tokens (90% discount)")
    print(f"[call_claude] Input: {usage.input_tokens}, Output: {usage.output_tokens} tokens")

    raw_text = response.content[0].text
    return extract_json(raw_text)


