def get_anthropic_api_key() -> str:
    key = os.environ.get("ANTHROPIC_API_KEY", "")
    if not key:
        cfg = load_config()
        key = cfg.get("anthropic", {}).get("api_key", "")
    if not key:
        raise RuntimeError(
            "ANTHROPIC_API_KEY not set. "
            "Set the environment variable or add anthropic.api_key to config.json"
        )
    return key


